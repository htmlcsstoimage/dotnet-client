namespace HtmlCssToImage.Models;


/// <summary>
/// Represents the units of measurement available for PDF dimensions.
/// </summary>
public enum PdfUnit
{

    /// <summary>
    /// px
    /// </summary>
    PIXELS,

    /// <summary>
    /// in
    /// </summary>
    INCHES,


    /// <summary>
    /// %
    /// </summary>
    PERCENTAGE,


    /// <summary>
    /// cm
    /// </summary>
    CENTIMETERS,


    /// <summary>
    /// mm
    /// </summary>
    MILLIMETERS,

    /// <summary>
    /// pt
    /// </summary>
    POINTS,
}

